## Summary results
umc_plot_clinicaltrials_sumres <- function (eutt_dataset, iv_dataset, iv_all_dataset, toggled_registry, umc, color_palette) {

    
    if (toggled_registry == "EUCTR") {

        dataset <- eutt_dataset %>%
            filter (date > as.Date("2022-02-18")-365*1.5) ## Only look at the last year and a half

        ## Only take the latest data point per month
        dataset$month <- dataset$date %>%
            format("%Y-%m")

        dataset <- dataset %>%
            group_by(city, month) %>%
            arrange(desc(date)) %>%
            slice_head() %>%
            ungroup()
        
        all_data <- dataset %>%
            group_by(date) %>%
            mutate(avg = round(100*sum(total_reported)/sum(total_due), digits=1)) %>%
            mutate(mouseover = paste0(sum(total_reported), "/", sum(total_due))) %>%
            slice_head() %>%
            select(date, hash, avg, month, total_due, total_reported, mouseover) %>%
            rename(percent_reported = avg) %>%
            mutate(city = "All") %>%
            ungroup()
        
        city_data <- dataset %>%
            group_by(date) %>%
            filter(city == umc) %>%
            mutate(mouseover = paste0(sum(total_reported), "/", sum(total_due))) %>% 
            select(date, hash, percent_reported, month,
                   total_due, total_reported, mouseover, city) %>% 
          ungroup()
        
    } else { ## The registry is not EUCTR

        dataset <- iv_dataset %>%
            filter(
                registry == toggled_registry,
                city == umc
            )

        min_year <- dataset$completion_year %>%
            min()

        max_year <- dataset$completion_year %>%
            max()

        city_data <- tribble(
            ~date, ~percent_reported, ~city
        )

        for (currentyear in seq(from=min_year, to=max_year)) {

            currentyear_trials <- dataset %>%
                filter(
                    completion_year <= currentyear
                )

            currentyear_denom <- nrow(currentyear_trials)

            currentyear_numer <- currentyear_trials %>%
                filter(
                  has_summary_results == TRUE
                  ) %>% 
              nrow()

            city_data <- city_data %>%
                bind_rows(
                    tribble(
                        ~date, ~percent_reported, ~city, ~mouseover,
                        currentyear, round(100*currentyear_numer/currentyear_denom), umc, paste0(currentyear_numer, "/", currentyear_denom)
                    )
                )
            
        }

        dataset <- iv_all_dataset %>%
            filter(
                registry == toggled_registry
            )
        
        all_data <- tribble(
            ~date, ~percent_reported, ~city
        )

        for (currentyear in seq(from=min_year, to=max_year)) {

            currentyear_trials <- dataset %>%
                filter(
                    completion_year <= currentyear
                )

            currentyear_denom <- nrow(currentyear_trials)

            currentyear_numer <- currentyear_trials %>%
                filter(
                  has_summary_results == TRUE
                  ) %>% 
              nrow()

            all_data <- all_data %>%
                bind_rows(
                    tribble(
                        ~date, ~percent_reported, ~city, ~mouseover,
                        currentyear, round(100*currentyear_numer/currentyear_denom), "All", paste0(currentyear_numer, "/", currentyear_denom)
                    )
                )
            
        }
        
    }

    # plot_data <- rbind(all_data, city_data)
    
    plot_ly(
      all_data,
      x = ~date,
      y = ~percent_reported,
      name = ~city,
      text = ~mouseover,
      type = 'scatter',
      mode = 'lines+markers',
      marker = list(
        color = color_palette[3],
        line = list(
          color = 'rgb(0,0,0)',
          width = 1.5
        )
      )
    ) %>%    
      add_trace(
        data = city_data,
        text = ~mouseover,
        marker = list(color = color_palette[2])
      ) %>%
        layout(
            xaxis = list(
                title = '<b>Date</b>'
            ),
            yaxis = list(
                title = '<b>Percentage of trials (%)</b>',
                range = c(0, 105)
            ),
            paper_bgcolor = color_palette[9],
            plot_bgcolor = color_palette[9],
            legend = list(xanchor= "left")
        )
    
}

